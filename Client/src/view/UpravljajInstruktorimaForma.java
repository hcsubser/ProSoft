    private void tblInstruktoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInstruktoriMouseClicked
        // TODO add your handling code here:
        int selektovanRed = tblInstruktori.getSelectedRow();
        if (selektovanRed != -1) {
            btnPromeni.setEnabled(true);
            if (btnObrisi.isVisible() == true) {
                btnObrisi.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tblInstruktoriMouseClicked

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        // TODO add your handling code here:
        try {
            int selektovanRed = tblInstruktori.getSelectedRow();
            if (selektovanRed == -1) {
                JOptionPane.showMessageDialog(this, "nista nije selektovano", "greska", JOptionPane.ERROR_MESSAGE);
                return;
            }

            TableModelInstruktor tmm = (TableModelInstruktor) tblInstruktori.getModel();
            Instruktor instruktorDelete = tmm.getInstruktor(selektovanRed);
            try {
                Controller.getInstance().obrisiInstruktora(instruktorDelete);
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete obrisati instruktora jer je povezan sa evidencijakursama", "Greska", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, "Instruktor obrisan", "greska", JOptionPane.INFORMATION_MESSAGE);

            //tma = new TableModelInstruktor();
            // tblInstruktori.setModel(tma);
            tmm.refresh();
            tblInstruktori.setModel(tmm);
        } catch (Exception ex) {
            Logger.getLogger(UpravljajInstruktorimaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

