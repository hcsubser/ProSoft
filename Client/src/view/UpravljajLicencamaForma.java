    private void btnPromeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromeniActionPerformed
        // TODO add your handling code here:
        int selektovanRed = tblLicenca.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Nista nije selektovano", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        TableModelLicenca model = (TableModelLicenca) tblLicenca.getModel();
        Licenca zaPromenu = model.getLicenca(selektovanRed);
        UbaciLicencuForma ussf = new UbaciLicencuForma(this, zaPromenu);
        ussf.setVisible(true);
    }//GEN-LAST:event_btnPromeniActionPerformed

    private void tblLicencaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLicencaMouseClicked
        // TODO add your handling code here:
        int selektovanRed = tblLicenca.getSelectedRow();
        if (selektovanRed != -1) {
            btnPromeni.setEnabled(true);
            if (btnObrisi.isVisible() == true) {
                btnObrisi.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tblLicencaMouseClicked

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        // TODO add your handling code here:
        try {
            int selektovanRed = tblLicenca.getSelectedRow();
            if (selektovanRed == -1) {
                JOptionPane.showMessageDialog(this, "Nista nije selektovano", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            TableModelLicenca model = (TableModelLicenca) tblLicenca.getModel();
            Licenca zaBrisanje = model.getLicenca(selektovanRed);
            try {
                Controller.getInstance().obrisiLicenca(zaBrisanje);
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete obrisati strucnu spremu jer je povezana sa instruktorima", "Greska", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, "Strucna sprema obrisana", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
            model.refresh();
            tblLicenca.setModel(model);
        } catch (Exception ex) {
            Logger.getLogger(UpravljajInstruktorimaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

